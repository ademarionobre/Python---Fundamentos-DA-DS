# Operações matemáticas (aritméticas)
"""
Soma: +
Subtração: -
Multiplicação: *
Divisão: /
Divisão inteira: //
Resto da divisão: %
Potência: **
"""
numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 // 3)
print(20 % 3)
print(2 ** 3)

